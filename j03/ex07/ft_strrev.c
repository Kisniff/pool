char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	c;

	a = 0;
	b = 0;
	while (str[a])
	{
		a++;
	}
	a = a - 1;
	while (b < a)
	{
		c = str[a];
		str[a] = str[b];
		str[b] = c;
		a--;
		b++;
	}
	return (str);
}
