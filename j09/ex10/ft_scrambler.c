void	ft_scrambler(int ***a, int *b, int *******c, int ****d)
{
	int tmp;

	tmp = c[0][0][0][0][0][0][0];
	c[0][0][0][0][0][0][0] = a[0][0][0];
	a[0][0][0] = b[0];
	b[0] = d[0][0][0][0];
	d[0][0][0][0] = tmp;
}
