#include "header.h"

int	addition(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	soustraction(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	multiplication(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	division(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	modulo(int nb1, int nb2)
{
	return (nb1 % nb2);
}
